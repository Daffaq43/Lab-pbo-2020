public class lat9Ifelse {
	public static void main(String[] args) {
		int number = 13;
		if (number%2==0) {
			System.out.println ("Angka genap");
		}else {
			System.out.println ("Angka ganjil");
		}
	}
}

/* program ini melibatkan ketentuan if else dalam penerapan nya, yaitu pada bilangan genap maupun ganjil. */
